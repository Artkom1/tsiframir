/**
 * PROMPTS GENERATOR
 * Creates optimized prompts for different calculator types
 */

function getPrompt(number, calculatorType, userData, person1Name, person2Name, calculationTrace) {
  const basePrompt = `
Дай подробный и профессиональный нумерологический анализ числа ${number}.

На основе:
- Расширенной системы нумерологии
- Книги "2х-цифровая трансформация личности" Сергея Кузнецова
- Лучших мировых практик нумерологии и психологии
- Глубокого понимания архетипов и жизненных путей

${calculationTrace ? `
ВАЖНО: Это число было вычислено следующим образом:
${calculationTrace}

Включи в своей анализ информацию о том как это число было рассчитано, чтобы подчеркнуть источник его энергии.
` : ''}

СТРУКТУРА ОТВЕТА (используй markdown):

## 🔮 Мистическое значение
(2-3 предложения о глубоком смысле этого числа в нумерологии)

## 💫 Психологический портрет

**Сильные стороны:**
- (3-4 ключевых качества личности с этим кодом)

**Области развития:**
- (3-4 области для личностного роста и трансформации)

**Поведенческие паттерны:**
- (2-3 типичных паттерна поведения)

## 🎯 Жизненный путь и судьба
(2-3 абзаца о том, как это число влияет на жизненный путь и духовное развитие человека)

## 💡 Практические рекомендации
(5-6 конкретных рекомендаций как применить эти знания в жизни)

## 🏢 Карьера и финансы
(Какие профессии раскроют максимум потенциала, как число влияет на финансовое благополучие)

## ❤️ Отношения и любовь
(Как это число проявляется в личных отношениях и с кем лучше всего совместимо)

## 🧘 Здоровье и благополучие
(Какие аспекты здоровья требуют внимания, рекомендации для балланса)

## 📖 Трансформационный урок
(Главный вывод и главный жизненный урок для этого числа)

${calculationTrace ? `
## 🔢 Как мы получили это число
В твоём анализе ОБЯЗАТЕЛЬНО объясни как это число было получено на основе следующих вычислений:
${calculationTrace}

Это поможет читателю понять источник энергии числа и его связь с исходными данными (словом, датой рождения и т.д.).
` : ''}

ВАЖНО:
- Будь вдохновляющим, но честным
- Дай конкретные советы, не абстрактные
- Используй психологический язык где возможно
- Говори о потенциале и трансформации, а не о судьбе как о нечто неизбежном
- Язык: Русский, грамотный, глубокий но понятный для образованной аудитории
- Целевая аудитория: люди 25-55 лет, интересующиеся личностным развитием

`;

  // Специальные промпты для совместимости
  if (calculatorType === 'compatibility' && person1Name && person2Name) {
    return `
Дай профессиональный анализ совместимости между ${person1Name} и ${person2Name}.

На основе нумерологических принципов из книги "2х-цифровая трансформация личности", дай подробный анализ:

## 👥 Анализ совместимости ${person1Name} и ${person2Name}

### 🔍 Числовой профиль
(Краткое описание профилей обоих людей и их числовых кодов)

### 💚 Гармонические аспекты
(Что объединяет эту пару, где они дополняют друг друга, сильные стороны отношений)

### ⚡ Точки напряженности
(Потенциальные конфликты и различия в подходе к жизни)

### 🎯 Путь развития пары
(Как эта пара может расти вместе, какие уроки предлежат им)

### 💑 Рекомендации для гармонии
(Конкретные совету как наладить отношения и создать взаимопонимание)

### 📊 Степень совместимости
(Оцени совместимость на основе нумерологических принципов)

Будь чувствительным, честным и конструктивным в анализе.
Помни, что люди ищут не приговор, а путь к улучшению отношений.
`;
  }

  // Промпт для анализа слова
  if (calculatorType === 'wordCode' && userData) {
    const prompt = basePrompt + `

СПЕЦИАЛЬНЫЙ КОНТЕКСТ: Это анализ нумерологического кода для слова "${userData}".

Дополнительно к структурированному анализу, добавь раздел:

## 📝 Значение слова в контексте
(Почему это слово имеет такую энергию, как его использование влияет на окружающих)

## 🔢 Как мы получили это число
(Объясни пошагово как буквы слова преобразуются в числа и как они суммируются, чтобы получить это число)
`;
    return prompt;
  }

  return basePrompt;
}

module.exports = {
  getPrompt
};
